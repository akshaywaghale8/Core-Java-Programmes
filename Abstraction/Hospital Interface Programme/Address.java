package com.akshu.interface_demo;

public class Address 
{
	private String Street;
	private String city;
	private String state;
	private int zipCode;
	
	
	public Address(String street, String city, String state, int zipCode) 
	{
		super();
		Street = street;
		this.city = city;
		this.state = state;
		this.zipCode = zipCode;
	}
	
	
	

}
